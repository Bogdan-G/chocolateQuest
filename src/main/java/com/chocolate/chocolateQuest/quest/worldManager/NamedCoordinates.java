package com.chocolate.chocolateQuest.quest.worldManager;


class NamedCoordinates {

   public int x;
   public int y;
   public int z;
   public String name;


   public NamedCoordinates(int x, int y, int z, String name) {
      this.x = x;
      this.y = y;
      this.z = z;
      this.name = name;
   }
}
